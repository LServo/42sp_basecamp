int ft_is_prime(int nb)
{
    int tmp;
    int soma;

    tmp = nb + 1;
    soma = 0;
    while (--tmp)
        if (tmp > 0 && nb % tmp == 0)
            soma += 1;
    if (soma == 2)
        return (1);
    return (0);
}