int ft_is_prime(int nb)
{
    int count;

    count = 2;
    if (nb < 2)
        return (0);
    while (count <= nb / 2 && count <= 46341)
    {
        if (nb % count == 0)
            return (0);
        count++;
    }
    return (1);
}
