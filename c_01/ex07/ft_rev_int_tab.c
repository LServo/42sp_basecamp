void ft_rev_int_tab(int *tab, int size)
{
    int x;
    int y;
    int i;

    i = 0;
    y = size - 1;
    while (i <= y)
    {
        x = tab[i];
        tab[i] = tab[y];
        tab[y] = x;
        y--;
        i++;
    }
}
